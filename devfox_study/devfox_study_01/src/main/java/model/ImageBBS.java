package model;

public class ImageBBS {
	private Integer writing_id;//投稿番号
	private Integer group_id;//グループ番号
	private Integer parent_id;//親の投稿番号
	private Integer order_no;//順序番号
	private String reg_date;//登録日
	private String id;//作成者
	private String image_name;//ファイル
	private String password;//パスワード
	private String title;//タイトル
	private String content;//内容
	public Integer getWriting_id() {
		return writing_id;
	}
	public void setWriting_id(Integer writing_id) {
		this.writing_id = writing_id;
	}
	public Integer getGroup_id() {
		return group_id;
	}
	public void setGroup_id(Integer group_id) {
		this.group_id = group_id;
	}
	public Integer getParent_id() {
		return parent_id;
	}
	public void setParent_id(Integer parent_id) {
		this.parent_id = parent_id;
	}
	public Integer getOrder_no() {
		return order_no;
	}
	public void setOrder_no(Integer order_no) {
		this.order_no = order_no;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getImage_name() {
		return image_name;
	}
	public void setImage_name(String image_name) {
		this.image_name = image_name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
}
